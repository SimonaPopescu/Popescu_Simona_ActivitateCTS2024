package classes;

public class Retragere {
    private int id;
    private float suma;

    public Retragere(int id, float suma) {
        this.id = id;
        this.suma = suma;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public float getSuma() {
        return suma;
    }

    public void setSuma(float suma) {
        this.suma = suma;
    }
}
